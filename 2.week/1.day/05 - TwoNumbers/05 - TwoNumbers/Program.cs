using System;

namespace _05___TwoNumbers
{
    class Program
    {
        static void Main(string[] args)
        {
            double thirteen = 13;
            double twentytwo = 22;

            Console.WriteLine(13 + 22);
            Console.WriteLine(22 - 13);
            Console.WriteLine(22 * 13);
            Console.WriteLine(twentytwo / thirteen);
            Console.WriteLine(22 / 13);
            Console.WriteLine(22 % 13);
            Console.ReadLine();
        }
    }
}
